DROP TABLE IF EXISTS lineitems;
DROP TABLE IF EXISTS orders;
DROP TABLE IF EXISTS items;
DROP TABLE IF EXISTS customers;
DROP TABLE IF EXISTS reps;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS user_roles;
DROP TABLE IF EXISTS regions;


CREATE TABLE `user_roles` (
  `user_role_id` int(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `user_role_name` varchar(30) NOT NULL,
  `user_role_description` varchar(200) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `users` (
  `user_id` int(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `user_first_name` varchar(30) NOT NULL,
  `user_last_name` varchar(30) NOT NULL,
  `user_email` varchar(100) NOT NULL,
  `user_password` char(255) NOT NULL,
  `user_salt` char(32) NOT NULL,
  `user_role_id` int(11) NOT NULL DEFAULT 1,
  `user_active` tinyint(1) NOT NULL DEFAULT 1
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

ALTER TABLE `users`
ADD CONSTRAINT `fk_user_roles` FOREIGN KEY IF NOT EXISTS
(`user_role_id`) REFERENCES `user_roles` (`user_role_id`);


CREATE TABLE `regions` (
  `region_id` int(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `region_name` varchar(30) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;


CREATE TABLE `reps` (
  `user_id` int(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `region_id` int(11) NOT NULL,
  `rep_commission` float(3,2) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

ALTER TABLE `reps`
ADD CONSTRAINT `fk_rep_users` FOREIGN KEY IF NOT EXISTS
(`user_id`) REFERENCES `users` (`user_id`);

ALTER TABLE `reps`
ADD CONSTRAINT `fk_regions` FOREIGN KEY IF NOT EXISTS
(`region_id`) REFERENCES `regions` (`region_id`);


CREATE TABLE `customers` (
  `user_id` int(11) DEFAULT NULL,
  `customer_store_name` varchar(30) DEFAULT NULL,
  `customer_phone_number` varchar(12) DEFAULT NULL,
  `customer_fax_number` varchar(12) DEFAULT NULL,
  `customer_address` varchar(150) DEFAULT NULL,
  `customer_city` varchar(16) DEFAULT NULL,
  `customer_state` varchar(15) DEFAULT NULL,
  `customer_postal_code` varchar(7) DEFAULT NULL,
  `customer_country` varchar(6) DEFAULT NULL,
  `customer_note` varchar(155) DEFAULT NULL,
  `rep_id` int(11) NOT NULL,
  `customer_credit_limit` mediumint(9) DEFAULT NULL,
  `customer_contract_date` date DEFAULT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

ALTER TABLE `customers`
ADD CONSTRAINT `fk_customer_users` FOREIGN KEY IF NOT EXISTS
(`user_id`) REFERENCES `users` (`user_id`);

ALTER TABLE `customers`
ADD CONSTRAINT `fk_customer_rep` FOREIGN KEY IF NOT EXISTS
(`rep_id`) REFERENCES `reps` (`user_id`);


CREATE TABLE `items` (
  `item_id` varchar(7) NOT NULL PRIMARY KEY,
  `item_description` varchar(100) NOT NULL,
  `item_unit_price` decimal(6,2) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE `orders` (
  `order_id` int(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
  `customer_user_id` int(11) NOT NULL,
  `rep_user_id` int(11) NOT NULL,
  `order_date` datetime NOT NULL,
  `order_shipping_date` datetime DEFAULT NULL,
  `order_shipping_method` varchar(7) DEFAULT NULL,
  `order_shipping_charge` tinyint(4) DEFAULT NULL,
  `order_terms` varchar(6) DEFAULT NULL,
  `order_note` varchar(96) DEFAULT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

ALTER TABLE `orders`
ADD CONSTRAINT `fk_order_customer` FOREIGN KEY IF NOT EXISTS
(`customer_user_id`) REFERENCES `customers` (`user_id`);


CREATE TABLE `lineitems` (
  `order_id` int(11) NOT NULL,
  `item_id` varchar(7) NOT NULL,
  `line_item_quantity` tinyint(4) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

ALTER TABLE `lineitems`
ADD CONSTRAINT `fk_order_lineitems` FOREIGN KEY IF NOT EXISTS
(`order_id`) REFERENCES `orders` (`order_id`);

ALTER TABLE `lineitems`
ADD CONSTRAINT `fk_item_lineitems` FOREIGN KEY IF NOT EXISTS
(`item_id`) REFERENCES `items` (`item_id`);



